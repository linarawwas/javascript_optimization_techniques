// ### Concatenation and Trimming Methods:

// 9. **String concat(string2, string3, ...)**
//    - Joins two or more strings and returns a new string.
//    - **Runtime:** O(n), where n is the total length of the concatenated strings.
const str1 = 'Hello,';
const str2 = ' World!';
const concatenated = str1.concat(str2);
console.log(concatenated); // Output: "Hello, World!"
